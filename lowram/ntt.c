static int16_t montgomery_reduce_small(int32_t a) {
  int16_t t;

  t = (int16_t)a * Q_INV_SMALL;
  t = (a - (int32_t)t * SMALL_Q) >> 16;
  return t;
}

static int16_t barrett_reduce(int16_t a) {
  int16_t t;
  const int16_t v = ((1 << 26) + SMALL_Q / 2) / SMALL_Q;

  t = ((int32_t)v * a + (1 << 25)) >> 26;
  t *= SMALL_Q;
  return a - t;
}

/*************************************************
 * Name:        fqmul
 *
 * Description: Multiplication followed by Montgomery reduction
 *
 * Arguments:   - int16_t a: first factor
 *              - int16_t b: second factor
 *
 * Returns 16-bit integer congruent to a*b*R^{-1} mod q
 **************************************************/
static int16_t fqmul(int16_t a, int16_t b) {
  return montgomery_reduce_small((int32_t)a * b);
}

/*************************************************
 * Name:        small_ntt
 *
 * Description: Inplace number-theoretic transform (NTT) in Rq.
 *              input is in standard order, output is in bitreversed order.
 *              This function is part of the lowram implementation.
 *
 * Arguments:   - int16_t r[256]: pointer to input/output vector of elements of Zq
 **************************************************/
void small_ntt(int16_t r[256]) {
  unsigned int len, start, j, k;
  int16_t t, zeta;

  k = 1;
  for(len = 128; len >= 2; len >>= 1) {
    for(start = 0; start < 256; start = j + len) {
      zeta = small_zetas[k++];
      for(j = start; j < start + len; j++) {
        t = fqmul(zeta, r[j + len]);
        r[j + len] = r[j] - t;
        r[j] = r[j] + t;
      }
    }
  }
}

/*************************************************
 * Name:        small_invntt_tomont
 *
 * Description: Inplace inverse number-theoretic transform in Rq and
 *              multiplication by Montgomery factor 2^16.
 *              Input is in bitreversed order, output is in standard order.
 *              This function is part of the lowram implementation.
 *
 * Arguments:   - int16_t r[256]: pointer to input/output vector of elements of Zq
 **************************************************/
void small_invntt_tomont(int16_t r[256]) {
  unsigned int start, len, j, k;
  int16_t t, zeta;
  const int16_t f = 1441; // mont^2/128

  k = 127;
  for(len = 2; len <= 128; len <<= 1) {
    for(start = 0; start < 256; start = j + len) {
      zeta = small_zetas[k--];
      for(j = start; j < start + len; j++) {
        t = r[j];
        r[j] = barrett_reduce(t + r[j + len]);
        r[j + len] = r[j + len] - t;
        r[j + len] = fqmul(zeta, r[j + len]);
      }
    }
  }

  for(j = 0; j < 256; j++) {
    r[j] = barrett_reduce(fqmul(r[j], f));
  }
}

/*************************************************
 * Name:        small_basemul
 *
 * Description: Multiplication of polynomials in Zq[X]/(X^2-zeta)
 *              used for multiplication of elements in Rq in NTT domain.
 *              This function is part of the lowram implementation.
 *
 * Arguments:   - int16_t r[2]: pointer to the output polynomial
 *              - const int16_t a[2]: pointer to the first factor
 *              - const int16_t b[2]: pointer to the second factor
 *              - int16_t zeta: integer defining the reduction polynomial
 **************************************************/
void small_basemul(int16_t r[2], const int16_t a[2], const int16_t b[2], int16_t zeta) {

  int16_t a0 = a[0], a1 = a[1];
  int16_t b0 = b[0];

  r[0] = fqmul(a1, b[1]);
  r[0] = fqmul(r[0], zeta);
  r[0] += fqmul(a0, b0);
  r[1] = fqmul(a0, b[1]);
  r[1] += fqmul(a1, b0);
}
